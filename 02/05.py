# 将数字类型转换为字符串
num_str = str(123)
print(num_str)
print(type(num_str))

# 将字符串转换为数字类型
str_num = int("111")
print(str_num)
print(type(str_num))

# 将整数类型转换为浮点类型
int_float = float(11)
print(int_float)
print(type(int_float))

# 将浮点类型转换为整数类型
# 会丢失精度
float_int = int(11.34)
print(float_int)
print(type(float_int))